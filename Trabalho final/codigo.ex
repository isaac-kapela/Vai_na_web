## pt2

for (let i = 0; i < tabuleiro.length; i++): Isso inicia um loop que vai de 0 até o 
número de linhas no tabuleiro (tabuleiro.length). A variável i é o índice da linha atual.

const linhaElemento = document.createElement('div');: Para cada iteração do loop, 
isso cria um novo elemento HTML <div> que representará uma linha no tabuleiro.
 Esse elemento é armazenado na variável linhaElemento.

linhaElemento.className = 'linha';:
 Define a classe CSS do elemento da linha como 'linha'. 
 Isso pode ser útil para aplicar estilos específicos às linhas do tabuleiro.

